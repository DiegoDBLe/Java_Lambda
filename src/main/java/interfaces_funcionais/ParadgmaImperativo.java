package interfaces_funcionais;

public class ParadgmaImperativo {
    public static void main(String[] args) {
        //Paradgma Imperativo: Instrução + Instrução = Instrução ou seja é que as instruções são executadas de formas instantaneas.
        //No momento em que são declarados ja são executadas
        int valor = 10;
        int resultado = valor *3;
        System.out.println("Resultado é :: " + resultado);
    }
}
